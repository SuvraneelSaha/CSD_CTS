package JavaTraining.Day1;

public class O3_Series {
  public static void main(String[] args) {
   //  2 7 3 8 4 9 

 
    for(int i = 1,a=7,b=2 ; i <= 10 ; i++)
    {
      if(i%2==0)
      {
        System.out.print(a+" ");
        a++;
      }
      else
      {
        System.out.print(b +" ");
        b++;
      }
    }
  }
  
}

